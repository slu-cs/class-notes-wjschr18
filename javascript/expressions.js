  // Expression syntax in Javascript

  // Booleans
  true
  false

  //Logic
  !false
  true && false
  true || false

  //Numbers (all one type)
  3.14

  //Equality
  3 === 3.0 // true
  3 !== 3.14

  //Ordering
  3 > 2
  3 >= 2
  2 < 3
  4 <= 4

  //Strings
  'hello'

  //Concatenation
  'a' + 'b' === 'ab'

  //Arithmetic
  3 + 2 === 5
  3 - 2 === 1
  3 * 2 === 6
  3 / 2 === 1.5
  3 % 2 === 1
  3 ** 2 === 9

  //Type coercion
  '3' + 2 === '32'
  '3' + true === '3true'
  '3' - 2 === 1
  3 + true === 4 // false === 0; true === 1

  //Loose Equality
  '3' == 3
  true == 1
  false == 0
  false == ''
  '' == 0

  //Template Strings
  '3+2 is ' + (3+2)
  `3+2 is ${3+2}`
  
